DROP TABLE IF EXISTS news;
DROP TABLE IF EXISTS sources;
DROP TABLE IF EXISTS topics;
CREATE TABLE news
(
    news_id   INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    source_id INTEGER,
    topic_id  INTEGER,
    CONSTRAINT pk_news PRIMARY KEY (news_id)
);

CREATE TABLE sources
(
    source_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name      VARCHAR(255),
    CONSTRAINT pk_sources PRIMARY KEY (source_id)
);
CREATE TABLE topics
(
    topic_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name     VARCHAR(255),
    CONSTRAINT pk_topics PRIMARY KEY (topic_id)
);
ALTER TABLE news
    ADD CONSTRAINT FK_NEWS_ON_SOURCE FOREIGN KEY (source_id) REFERENCES sources (source_id) ON UPDATE CASCADE;

ALTER TABLE news
    ADD CONSTRAINT FK_NEWS_ON_TOPIC FOREIGN KEY (topic_id) REFERENCES topics (topic_id) ON UPDATE CASCADE ;
